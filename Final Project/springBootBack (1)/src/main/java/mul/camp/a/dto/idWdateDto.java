package mul.camp.a.dto;

public class idWdateDto {
	private String wdate;
	private String id;
	
	public idWdateDto() {
		
	}

	public idWdateDto(String wdate, String id) {
		super();
		this.wdate = wdate;
		this.id = id;
	}

	public String getWdate() {
		return wdate;
	}

	public void setWdate(String wdate) {
		this.wdate = wdate;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}
	
	
}

